#!/usr/bin/env python3
# -*- coding: utf-8 -*-
# 증권/시황 노이즈 수집 제외 필터 — SSOT (운영자 260701)
# 증권사 리포트·개별종목 시황·증권사 홍보/인사·기업 자기홍보(최우수) = 뉴스 큐레이션 노이즈
#   → 수집 단계(knews_scraper.collect)에서 아예 제외 + to_candidates 저장 시 기존분도 정리.
# 정확한 키워드 + AND 조건(오탐 방지). dry-run 실측(candidates 2821건) 오탐 0.
# ⚠️ "증권/투자" 단독은 신호 아님 — 해외직접투자·메가투자·증권거래세 등 정상 뉴스는 보존.
# ⚠️ 개별종목 급등락은 지수(코스피·코스닥) 맥락이면 유지 — "코스피 급락에 삼성전자 폭락"=유지, "SK하이닉스 11% 급락"=제외.
import re

# ── ① 단독 제외 — 태그·전용어(실측 오탐 0) ──
_SOLO = re.compile(
    r"\[특징주"          # 개별종목 시황 코너
    r"|\[C컷"            # 사진 기사
    r"|\[인사"           # 인사 발령 명단 전체(운영자 260701 '싹다' — 증권 무관 기관 포함)
    r"|\[서울데이터랩"   # 증권 데이터 코너
    r"|\[마감시황"       # 정규 시황 정리(서킷·사이드카 '발동' 속보는 태그 없어 유지)
    r"|\[핫스탁"         # 개별종목 시황 코너
    r"|목표가|목표주가"  # 증권사 목표주가 리포트
    r"|대장주"           # 개별종목 시총 순위
    r"|證"               # 증권사 약칭(한자 = 뉴스 제목에서 증권사 전용)
    r"|수혜주|관련주|테마주"   # 개별종목 테마
    r"|한양증권"         # 운영자 지정(회사 이슈만 나오는 매체)
)

# ── ② 증권사 AND — (증권사어) AND (리포트|홍보|임원인사). "증권거래세"(세금뉴스)는 예외 보존. ──
_BROKER = re.compile(r"증권|투자증권|금융투자")
_BROKER_EXCEPT = re.compile(r"증권거래세")
_BROKER_SIG = re.compile(
    r"투자의견|비중\s?확대|비중\s?축소|밸류에이션|매수의견|매도의견|눈높이|톱픽|저평가"   # 리포트어
    r"|기부|이벤트|캐시백|발행어음|어음|MOU|상호협력|협력|맞손|IR|ETF"                    # 회사홍보어
    r"|선임|경영체제|조직개편|대표이사|각자\s?대표"                                        # 임원인사어(운영자 260701)
)

# ── ③ 기업명 AND — (주요 기업/종목명) AND (최우수 | 개별종목 급등락) ──
#   ※ "협업"은 규칙에서 뺌(LG전자-엔비디아 등 중요 협업까지 걸려 운영자 260701 제거).
#   ※ 기업명 리스트라 스포츠("대한민국 32강 확률 급락")·부동산("동탄 급등")은 안 걸림(오탐 방지).
_COMPANY = re.compile(
    r"삼성전자|SK하이닉스|삼전닉스|삼전·닉스|SK스퀘어|LG전자|LG에너지솔루션|LG화학|삼성SDI"
    r"|삼성바이오로직스|삼성바이오|삼성전기|삼성물산|삼성생명|현대차|기아|현대모비스|현대백화점"
    r"|셀트리온|포스코|POSCO|네이버|NAVER|카카오페이|카카오|에코프로비엠|에코프로|한화에어로"
    r"|두산에너빌리티|두산|HD현대|크래프톤|대한항공|GS리테일|GS25|롯데쇼핑|신세계|CJ온스타일|CJ"
    r"|오뚜기|뉴온|원익IPS|스트라드비젼|매드업|져스텍|코퍼스코리아"
)
_COMPANY_PROMO = re.compile(r"최우수")               # 기업 자기홍보(수상·선정)
_MOVE = re.compile(r"급등|급락|폭등|폭락|급반등|상한가|하한가")
_INDEX = re.compile(r"코스피|코스닥|증시|지수")       # 지수 맥락이면 유지(=개별종목 아님)


def is_excluded_title(title):
    """증권/시황 노이즈면 True → 수집 제외. 정확한 키워드 + AND(오탐 방지)."""
    t = title or ""
    if _SOLO.search(t):
        return True
    if _BROKER.search(t) and not _BROKER_EXCEPT.search(t) and _BROKER_SIG.search(t):
        return True
    if _COMPANY.search(t):
        if _COMPANY_PROMO.search(t):
            return True
        if _MOVE.search(t) and not _INDEX.search(t):
            return True
    return False
